    internal class Methods
    {
        public static int Modulus(int num1, int num2)
        {
          //remainder after they're divided. rounded up or down to closest

          // 10/5 = 2r0
          // 10% 5 = 0
          // 7/5 = 1r2
          // 7 % 5 = 2
            return num1 % num2;
        }

    
        
